#ifndef LOS_TYPES_H
#define LOS_TYPES_H

/*
 * This take for granted that the compiler is i686-elf
 */

typedef int int32_t;
typedef unsigned int uint32_t;

typedef short int int16_t;
typedef unsigned short int uint16_t;

typedef char int8_t;
typedef unsigned char uint8_t;

typedef unsigned char BOOL;
#define TRUE    1
#define FALSE   0

#endif